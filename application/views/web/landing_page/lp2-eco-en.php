            <p>We actively use recycled materials for our production, creating demand for recycled plastic materials from our vicinity. Our suppliers have recycling capacities of up to 10.000 tons per month. With this effort, we hope that we can reduce the number of plastic bag thrown into the ocean in particular and our environment in general. </p>
            <p>We also believe that recycled plastic is better than any biodegradable solution. This is because biodegradable plastics are not recycleable and under UV-light, they break down into smaller microplastic and side products which are actually toxic for our watersupply<sup>1</sup>. This will create further problems to our environment since microplastics are very small and very hard to detect with just naked eye, while basic plastic products can be recycled on and on using the correct collection and waste handling methods. Hence, the current plastic problems can be solved with proper circular economy implementation.</p>
